    public void LlenarUsuarios() {

        DefaultComboBoxModel modelo = (DefaultComboBoxModel) cb_users.getModel();
        modelo.removeAllElements();
        for (User u : userAdministrador.getUsersList()) {
            modelo.addElement(u);
        }
    }

