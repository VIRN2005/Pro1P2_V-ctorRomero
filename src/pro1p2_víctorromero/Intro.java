    public void Disparo(DisparoLanzaguisantes gm, MoveZombies mz) {
        if (gm.getX() == mz.getX()) {
            zombies.setHealth(zombies.getHealth() - 25);
        }
        if (zombies.getHealth() <= 0) {
            mz.getLabel().setVisible(false);
        }
        System.out.println(gm.getX() + "---->" + gm.getY());
    }

